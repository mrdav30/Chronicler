namespace Chronicler;

/// <summary>
/// Describes whether a chronicler pass is saving values out or loading values in.
/// </summary>
public enum SerializationMode
{
    /// <summary>
    /// The chronicler is saving values out.
    /// </summary>
    Saving,
    /// <summary>
    /// The chronicler is loading values in.
    /// </summary>
    Loading
}
